# Build rows with sequential codes per prefix
rows = []
counters = {}
for cat, items in categories.items():
    for prefix, desc_base, unit, qty_range in items:
        counters[prefix] = counters.get(prefix, 0) + 1
        n = counters[prefix]
        code = f"{prefix}.{n:03d}"
        qty = random.randint(*qty_range)
        rows.append((cat, code, desc_base, unit, qty))

# Pad to 1000 with realistic area/discipline variants
area_suffixes = [
    "Area A", "Area B", "Area C", "Area D",
    "Train 1", "Train 2", "Train 3",
    "North Battery", "South Battery",
    "Wellpad 1", "Wellpad 2", "Wellpad 3",
    "Manifold Area", "Slug Catcher Area",
    "Export Pump Area", "Utilities Area",
]

base_pool = rows[:]
while len(rows) < 100_000:
    base_cat, base_code, base_desc, base_unit, base_qty = random.choice(base_pool)
    suffix = random.choice(area_suffixes)
    prefix = base_code.split(".")[0]
    counters[prefix] = counters.get(prefix, 0) + 1
    new_code = f"{prefix}.{counters[prefix]:03d}"
    new_desc = f"{base_desc} - {suffix}"
    new_qty = int(base_qty * random.uniform(0.5, 1.5))
    rows.append((base_cat, new_code, new_desc, base_unit, new_qty))

rows = rows[:100_000]

# Verify uniqueness
codes = [r[1] for r in rows]
assert len(set(codes)) == len(codes), f"Duplicate codes found! {len(codes) - len(set(codes))} duplicates"

with open("data/boq_oil_gas_100k.csv", "w", newline="", encoding="utf-8") as f:
    writer = csv.writer(f)
    writer.writerow(["category", "code", "description", "unit", "budget_quantity"])
    for cat, code, desc, unit, qty in rows:
        writer.writerow([cat, code, desc, unit, qty])

print(f"Done: {len(rows)} rows, {len(set(r[0] for r in rows))} categories, {len(set(codes))} unique codes")
